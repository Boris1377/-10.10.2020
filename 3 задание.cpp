#include <iostream>

int main() {
    int n1=0, n2=0, n3=0;
    cout << "input number 1:";
    cin >> n1;
    cout << "input number 2:";
    cin >> n2;
    cout << "input number 3:";
    cin >> n3;
    cout << "n1 * 2=" << n1 * 2<< endl;
    cout << "n2 - 3=" << n2 - 3<< endl;
    cout << "n3 * n3=" << n3 * n3<< endl;
    cout << "sum of three new numbers:" << (n1 * 2) + (n2 - 3) + (n3 * n3)<< endl;

    return 0;
}
